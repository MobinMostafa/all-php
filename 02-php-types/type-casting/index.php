<?php 

// Type casting allows you to convert a value of one type to another

//To cast a value to an integer, you use the (int) type casting operator.

$num = (int) 12.5;

echo $num;

//To cast a value to a float, you use the (float) operator.

echo "<br>";
$amount = (float) 100;

echo $amount;

//To cast a value to a string, you use the (string) operator.

echo "<br>";

$amounts = 100;
echo (string)$amounts . " USD";

echo "<br>";
